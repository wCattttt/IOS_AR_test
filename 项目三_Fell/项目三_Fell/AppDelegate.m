    [self.window makeKeyAndVisible];
    
    self.window.rootViewController = [[MainTabBarController alloc] init];
    
    
    // 集成Share SDK
    //注册ShareSDK的AppKey
    [ShareSDK registerApp:@"a838b1ea9876"];
    
    //1.添加新浪微博应用 注册网址 http://open.weibo.com
